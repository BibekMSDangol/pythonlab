a = int(input("Enter value:"))
if a == 1:
    print("It is Sunday")
elif a == 2:
    print("It is Monday")
elif a == 3:
    print("It is Tuesday")
elif a == 4:
    print("It is Wednesday")
elif a == 5:
    print("It is Thursday")
elif a == 6:
    print("It is Friday")
else:
    print("It is Saturday")